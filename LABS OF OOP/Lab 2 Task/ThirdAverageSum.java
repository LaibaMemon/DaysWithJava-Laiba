class ThirdAverageSum
{
public static void main(String args[])
{
   float a=2.2f;
   float b=2.3f;
   float c=2.4f;
   float d=2.5f;
   float e=2.6f;
   double sum=a+b+c+d+e;
   double average=sum/5;

  System.out.println("Sum is:"+sum);
  System.out.println("Average is:"+average);
}
}
