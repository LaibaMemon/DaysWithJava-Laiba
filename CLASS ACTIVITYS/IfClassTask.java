class IfClassTask
{
public static void main(String args[])
{
  int salary=40000;
  if (salary>=20000  &&  salary<=30000)
{
  System.out.println("Manager Operation:");
}
  else if
  (salary>=30000    &&   salary<=40000)
{
  System.out.println("Manager");
}
  else if
   (salary>=40000   && salary<=50000)
{
   System.out.println("Area Manager:");
}
  else if
  (salary>=50000)
{
 System.out.println("Regional Operator:");
}
}
}
