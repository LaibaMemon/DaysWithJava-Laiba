class Conversion
{
	public static void main(String args[])
	{
	   int a=10;
	   byte b=(byte)a;

	   System.out.println(b);



	   byte c=20;
	   int d=(int)c;
	   System.out.println(d);



	   double e=10.2;
	   float f=(float)e;
	   System.out.println(f);



	   int g=10;
	   float h=(float)g;
	   System.out.println(h);
	   	}
}